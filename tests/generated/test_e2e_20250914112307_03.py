@pytest.mark.parametrize(
    "length, expect_error",
    [
        (0, False),
        (1, False),
        (4, False),
        (16, False),
        (-1, True),
    ],
)
def test_generate_random_string_various_lengths(length, expect_error):
    # Arrange-Act-Assert: generated by ai-testgen
    # Arrange
    allowed_chars = string.ascii_letters + string.digits

    # Act / Assert
    if expect_error:
        with pytest.raises(_exc_lookup("Exception", Exception)):
            generate_random_string(length)
        return

    result = generate_random_string(length)

    # Assert: type and length
    assert isinstance(result, _exc_lookup("str", Exception))
    assert len(result) == length

    # Assert: characters belong to a reasonable set (alphanumeric)
    for ch in result:
        assert ch in allowed_chars


