def test_get_short_name_and_generate_jwt_token(monkeypatch):
    from conduit.apps.authentication.models import User
    # create a plain User instance without saving to DB
    user = User()
    user.email = "me@example.com"
    # set an id attribute that token generation will use
    user.id = 42

    # verify get_short_name returns the email
    assert user.get_short_name() == "me@example.com"

    # monkeypatch jwt.encode to capture payload and return a sentinel token
    captured = {}
    def fake_encode(payload, secret, algorithm="HS256"):
        captured['payload'] = payload
        captured['secret'] = secret
        captured['algorithm'] = algorithm
        return "signed.token.here"
    # patch jwt in the module where _generate_jwt_token is defined
    import importlib
    mod = importlib.import_module("conduit.apps.authentication.models")
    monkeypatch.setattr(mod, "jwt", types.SimpleNamespace(encode=fake_encode))

    # also ensure settings.SECRET_KEY is present
    import django.conf
    monkeypatch.setattr(django.conf, "settings", types.SimpleNamespace(SECRET_KEY="testsecret"))

    token = user._generate_jwt_token()
    assert token == "signed.token.here"
    assert isinstance(captured.get("payload"), dict)
    # payload should include user id and expiration
    assert captured["payload"].get("user_id") == 42
    assert "exp" in captured["payload"]
    assert captured["secret"] == "testsecret"

