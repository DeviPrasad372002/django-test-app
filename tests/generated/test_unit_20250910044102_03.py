def test_generate_random_string_and_core_exception_handlers():
    from conduit.apps.core.utils import generate_random_string
    from conduit.apps.core.exceptions import core_exception_handler, _handle_generic_error, _handle_not_found_error
    # generate_random_string produces the correct length and allowed characters
    s = generate_random_string(12)
    assert isinstance(s, str)
    assert len(s) == 12
    allowed = set(string.ascii_letters + string.digits)
    assert set(s).issubset(allowed)

    # _handle_generic_error should return a dict-like structure with errors
    gen = _handle_generic_error(Exception("boom"))
    assert isinstance(gen, dict)
    assert 'errors' in gen

    # _handle_not_found_error should return a dict-like structure with errors
    nf = _handle_not_found_error(Exception("not found"))
    assert isinstance(nf, dict)
    assert 'errors' in nf

    # core_exception_handler should produce a response-like object for a NotFound exception
    _NotFound = builtins._exc_lookup('NotFound', Exception)
    exc = _NotFound("missing")
    # context can be empty dict
    resp = core_exception_handler(exc, context={})
    # handler may return a Response or dict; accept either but ensure it communicates errors
    if resp is None:
        pytest.skip("core_exception_handler returned None in this environment")
    # If it's a DRF Response-like object, it will have .data and .status_code
    if hasattr(resp, "data"):
        assert isinstance(resp.data, (dict, list))
    else:
        # otherwise assume it's a dict
        assert isinstance(resp, dict) and ('errors' in resp or 'detail' in resp)


